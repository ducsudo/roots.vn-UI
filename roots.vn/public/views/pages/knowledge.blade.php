                $("#tab-1 svg path").css("stroke", "#E3C161");
                $("#show-tab-1").removeClass("hidden");
            })

            // Show tab2
            $("#tab-2").click(function() {
                $('#tab-1').removeClass('bg-white');
                $('#tab-1 p').removeClass('text-[#E2AC36]');
                $("#tab-1 svg path").css("stroke", "#BFBFBF");
